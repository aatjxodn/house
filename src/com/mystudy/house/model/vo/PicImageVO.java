package com.mystudy.house.model.vo;

public class PicImageVO {
	private String imageIdx, pictureIdx, imagefile;

	public String getImageIdx() {
		return imageIdx;
	}

	public void setImageIdx(String imageIdx) {
		this.imageIdx = imageIdx;
	}

	public String getPictureIdx() {
		return pictureIdx;
	}

	public void setPictureIdx(String pictureIdx) {
		this.pictureIdx = pictureIdx;
	}

	public String getImagefile() {
		return imagefile;
	}

	public void setImagefile(String imagefile) {
		this.imagefile = imagefile;
	}

}
